import random

def number_guesser():
    """
    A number guessing game where the program generates a random number within a user-specified range,
    and the user tries to guess it.
    """
    print("Welcome to the Number Guesser Game!")
    
    # Prompt the user to define the range
    try:
        lower_bound = int(input("Enter the lower bound of the range: "))
        upper_bound = int(input("Enter the upper bound of the range: "))
        
        if lower_bound >= upper_bound:
            print("Invalid range! The lower bound must be less than the upper bound.")
            return
        
        # Generate a random number within the specified range
        random_number = random.randint(lower_bound, upper_bound)
        attempts = 0

        print(f"Great! I have selected a number between {lower_bound} and {upper_bound}. Try to guess it!")
        
        while True:
            try:
                # Get the user's guess
                guess = int(input("Enter your guess: "))
                attempts += 1

                if guess < lower_bound or guess > upper_bound:
                    print(f"Please enter a number within the range {lower_bound} to {upper_bound}.")
                elif guess < random_number:
                    print("Too low! Try again.")
                elif guess > random_number:
                    print("Too high! Try again.")
                else:
                    print(f"Congratulations! You guessed the number {random_number} in {attempts} attempts.")
                    break
            except ValueError:
                print("Invalid input. Please enter a valid number.")
    except ValueError:
        print("Invalid input. Please enter valid integers for the range.")

# Run the number guessing game
number_guesser()
